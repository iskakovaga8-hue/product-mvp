export function generateRecommendations(
  sectionScores: Record<string, number>
): Record<string, Recommendation> {
  const result: Record<string, Recommendation> = {};

  for (const [key, score] of Object.entries(sectionScores)) {
    let level: "strong" | "developing" | "needs_work";
    if (score >= 75) {
      level = "strong";
    } else if (score >= 50) {
      level = "developing";
    } else {
      level = "needs_work";
    }

    result[key] = RECOMMENDATION_MAP[key]?.[level] ?? {
      level,
      text: "Рекомендация недоступна.",
    };
  }

  return result;
}
